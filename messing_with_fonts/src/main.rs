#[macroquad::main("font")]
async fn main() {
    let font = FONT
        .iter()
        .map(|character| {
            Texture2D::from_rgba8(
                8,
                13,
                &character
                    .iter()
                    .flat_map(|row| {
                        (0..8).flat_map(move |x| std::iter::repeat((row >> x & 1) * 255).take(4))
                    })
                    .collect::<Vec<u8>>(),
            )
        })
        .collect::<Vec<_>>();

    loop {
        draw_text_custom(
            &font,
            "abcdefghijklmnopqrstuvwxyz",
            screen_width() / 10.,
            screen_height() / 10.,
        );
        draw_text_custom(
            &font,
            "ABCDEFGHIJKLMNOPQRSTUVWXYZ",
            screen_width() / 10.,
            (screen_height() / 10.) + 50.,
        );
        draw_text_custom(
            &font,
            "1234567890",
            screen_width() / 10.,
            (screen_height() / 10.) + 100.,
        );
        draw_text_custom(
            &font,
            "!@#$%^&*()",
            screen_width() / 10.,
            (screen_height() / 10.) + 150.,
        );
        draw_text_custom(
            &font,
            "[]{};':\",.<>/?-_=+",
            screen_width() / 10.,
            (screen_height() / 10.) + 200.,
        );

        next_frame().await;
    }
}

fn draw_text_custom(font: &Vec<Texture2D>, text: &str, x: f32, y: f32) {
    let mut x = x;
    for c in text.chars() {
        let c = c as usize;
        assert!(c < 128);
        draw_texture_ex(
            font[c],
            x,
            y,
            WHITE,
            DrawTextureParams {
                dest_size: Some(Vec2::new(8. * (50. / 13.), 50.)),
                source: None,
                rotation: 0.,
                flip_x: true,
                flip_y: true,
                pivot: None,
            },
        );
        x += 9. * (50. / 13.);
    }
}
